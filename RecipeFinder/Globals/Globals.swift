import UIKit




// Fonts
extension UIFont {
    static var headerFont: UIFont {
        return UIFont.boldSystemFont(ofSize: 18)
    }
    
    static var titleFont: UIFont {
        return UIFont.boldSystemFont(ofSize: 16)
    }
    
    static var bodyFont: UIFont {
        return UIFont.systemFont(ofSize: 14)
    }
    
}
